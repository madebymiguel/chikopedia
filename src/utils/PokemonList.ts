  private static instance: PokemonList;

  private numToName: Map<number, string>;
  private nameToNum: Map<string, number>;
  private numToFormCount: Map<number, number>;

  private constructor() {
    this.numToName = new Map<number, string>();
    this.nameToNum = new Map<string, number>();
    this.numToFormCount = new Map<number, number>();

    for (let i = 0; i < PokemonList.list.length; i++) {
      const entry: PokemonListEntry = PokemonList.list[i];
      if (this.numToName.has(entry.num)) {
        this.numToFormCount.set(
          entry.num,
          this.numToFormCount.get(entry.num + 1) || 1
        );
      } else {
        this.numToName.set(entry.num, entry.name);
        this.nameToNum.set(entry.name, entry.num);
        this.numToFormCount.set(entry.num, 1);
      }
    }
  }

  static getList(): PokemonList {
    if (!PokemonList.instance) {
      PokemonList.instance = new PokemonList();
    }

    return PokemonList.instance;
  }

  getNameFromNum(num: number): string {
    return this.numToName.get(num) || "";
  }

  getNumFromName(name: string): number {
    return this.nameToNum.get(name) || 0;
  }

  getFormCount(id: number | string): number {
    if (typeof id === "number" && this.numToFormCount.has(id)) {
      return this.numToFormCount.get(id) || 0;
    } else if (typeof id === "string") {
      return this.numToFormCount.get(this.nameToNum.get(id) || 0) || 0;
    } else {
      return 0;
    }
  }

  getMaxFormCount(): number {
    return Math.max(...this.numToFormCount.values());
  }

  get size(): number {
    return this.nameToNum.size;
  }
